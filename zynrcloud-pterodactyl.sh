StartLimitInterval=180
StartLimitBurst=30
RestartSec=5s

[Install]
WantedBy=multi-user.target
PTEROQEOF
    systemctl daemon-reload
    systemctl enable --now pteroq &>/dev/null
    ok "pteroq enabled + started"

    # 17 в”Җв”Җ Nginx vhost
    case "$USE_CF" in

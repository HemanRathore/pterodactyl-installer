# в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ
status_check() {
    show_banner; echo -e "${BOLD}${BLUE}  рҹ“Ҡ STATUS & HEALTH CHECK${RESET}"; sep
    _chk() {
        local st; st=$(systemctl is-active "$2" 2>/dev/null || echo "not-found")
        [ "$st" = "active" ] \
            && printf "  ${GREEN}[вң”]${RESET} %-22s ${GREEN}%s${RESET}\n" "$1" "$st" \
            || printf "  ${RED}[вңҳ]${RESET} %-22s ${RED}%s${RESET}\n" "$1" "$st"
    }
    echo -e "\n  ${WHITE}в”Җв”Җ Services в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${RESET}"
    _chk "Nginx"           nginx
    _chk "PHP 8.2-FPM"     php8.2-fpm
    _chk "MariaDB"         mariadb
    _chk "Redis"           redis-server
    _chk "pteroq"          pteroq
    _chk "Wings"           wings
    _chk "Docker"          docker
    _chk "Cloudflared"     cloudflared
    echo -e "\n  ${WHITE}в”Җв”Җ Resources в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${RESET}"
    df -h / | tail -1 | awk '{printf "  рҹ’ҫ Disk : %s / %s (%s)\n",$3,$2,$5}'
    free -h | awk '/^Mem:/{printf "  рҹ§  RAM  : %s / %s\n",$3,$2}'
    echo -e "  вҡҷпёҸ  CPUs : $(nproc)  |  Load:$(uptime | awk -F'load average:' '{print $2}')"
    echo -e "  рҹҢҗ IP   : $(hostname -I | awk '{print $1}')  |  $(hostname)"
    echo -e "\n  ${WHITE}в”Җв”Җ Versions в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җ${RESET}"
    [ -f /var/www/pterodactyl/config/app.php ] \
        && echo -e "  рҹҡҖ Panel : ${CYAN}v$(grep "'version'" /var/www/pterodactyl/config/app.php | head -1 | awk -F"'" '{print $4}')${RESET}" \
        || echo -e "  рҹҡҖ Panel : ${RED}Not installed${RESET}"
    command -v wings &>/dev/null \
        && echo -e "  рҹ”§ Wings : ${CYAN}$(wings --version 2>/dev/null)${RESET}" \
        || echo -e "  рҹ”§ Wings : ${RED}Not installed${RESET}"
    command -v docker &>/dev/null \
        && echo -e "  рҹҗі Docker: ${CYAN}$(docker --version 2>/dev/null | awk '{print $3}' | tr -d ',')${RESET}  ($(docker ps -q 2>/dev/null | wc -l) running)" \
        || echo -e "  рҹҗі Docker: ${RED}Not installed${RESET}"
    command -v cloudflared &>/dev/null \
        && echo -e "  вҳҒпёҸ  CF    : ${CYAN}$(cloudflared --version 2>/dev/null | head -1)${RESET}" \
        || echo -e "  вҳҒпёҸ  CF    : ${DIM}Not installed${RESET}"
    sep; echo -e "${CYAN}  вҳ… ZynrCloud вҖ” https://zynrcloud.com${RESET}"; pause
}


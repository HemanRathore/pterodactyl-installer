# Spinner вҖ” call after backgrounding a command with &
spinner() {
    local pid=$! sp='в Ӣв ҷв №в ёв јв ҙв Ұв §в Үв Ҹ' i=0
    while kill -0 "$pid" 2>/dev/null; do
        printf "\r  ${CYAN}[%s]${RESET} %s..." "${sp:i++%${#sp}:1}" "$1"
        sleep 0.1
    done
    wait "$pid"; local rc=$?
    [ $rc -eq 0 ] \
        && printf "\r  ${GREEN}[вң”]${RESET} %-55s\n" "$1" \
        || printf "\r  ${RED}[вңҳ]${RESET} %-55s (exit $rc)\n" "$1 FAILED"
    return $rc
}

